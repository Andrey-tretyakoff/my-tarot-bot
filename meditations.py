def get_meditation_of_day(date: datetime = None) -> dict:
    """Возвращает медитацию, подходящую под текущую Луну"""
    if date is None:
        date = datetime.now()

    # 1. Пытаемся получить данные о Луне
    try:
        from lunar import get_lunar_day
        lunar_info = get_lunar_day(date)
        lunar_day_num = lunar_info['lunar_day']
        phase_str = lunar_info['phase']  # Например: "🌒 Растущая"
        is_magic = lunar_info.get('is_magic_day', False)
    except Exception:
        # Если lunar.py нет или ошибка, берем случайную универсальную
        lunar_day_num = date.day
        phase_str = ""
        is_magic = False

    # 2. Определяем теги текущей фазы
    current_tags = []
    if "Растущая" in phase_str:
        current_tags.append('waxing')
    elif "Убывающая" in phase_str:
        current_tags.append('waning')

    # Специфичные дни
    if lunar_day_num in [1, 29, 30]:
        current_tags.append('new')
    if lunar_day_num in [15, 16]:
        current_tags.append('full')
    if is_magic:
        current_tags.append('magic')

    current_tags.append('all')  # Всегда ищем и универсальные

    # 3. Фильтруем базу: ищем медитации, у которых есть хотя бы один общий тег
    valid_meditations = []
    for m in MEDITATIONS_DB:
        if any(tag in m['tags'] for tag in current_tags):
            valid_meditations.append(m)

    # Если ничего не подошло (страховка), берем всё
    if not valid_meditations:
        valid_meditations = MEDITATIONS_DB

    # 4. Выбираем медитацию по кругу на основе лунного дня
    # (lunar_day_num - 1) % len(...) гарантирует, что каждый лунный день будет новая практика
    index = (lunar_day_num - 1) % len(valid_meditations)

    return valid_meditations[index]