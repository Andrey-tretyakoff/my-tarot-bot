def get_weekly_rune_spread(zodiac: str, birth_date: str, gender: str):
    """Возвращает готовый HTML-текст расклада и список названий рун для картинки"""
    # Фиксируем сид на неделю, чтобы расклад не менялся до понедельника
    current_week = datetime.now(MSK).isocalendar()[1]
    seed_str = f"{birth_date}_{current_week}_{zodiac}"
    seed = sum(ord(c) for c in seed_str)
    random.seed(seed)

    pool = RUNES_DB.copy()
    r1 = random.choice(pool);
    pool.remove(r1)
    r2 = random.choice(pool);
    pool.remove(r2)
    r3 = random.choice(pool)

    # Персонализация
    if gender == "male":
        greet = "Брат, руны говорят тебе:"
        focus = "твоя сила воли и действие"
    elif gender == "female":
        greet = "Сестра, послушай свою интуицию:"
        focus = "твоя мудрость и внутренний поток"
    else:
        greet = "Путник, обрати внимание на знаки:"
        focus = "твой баланс и осознанность"

    text = f"🔮 <b>РУНИЧЕСКИЙ РАСКЛАД НЕДЕЛИ</b>\n\n"
    text += f"♈️ <b>Знак:</b> {zodiac.capitalize()} | 🎂 <b>Дата:</b> {birth_date}\n"
    text += f"✨ {greet}\n\n"

    text += f"1️⃣ <b>СИТУАЦИЯ:</b> {r1['sym']} {r1['ru']}\n<i>{r1['mean']}</i>\n{r1['adv']}\n\n"
    text += f"2️⃣ <b>ДЕЙСТВИЕ:</b> {r2['sym']} {r2['ru']}\n<i>{r2['mean']}</i>\nОпора на {focus}.\n\n"
    text += f"3️⃣ <b>ИТОГ:</b> {r3['sym']} {r3['ru']}\n<i>{r3['mean']}</i>\n{r3['adv']}\n\n"

    text += "💎 <b>СОВЕТ НЕДЕЛИ:</b>\n"
    symbols = [r1['sym'], r2['sym'], r3['sym']]
    if any(s in symbols for s in ["ᚲ", "", "ᚦ"]):
        text += "🔥 В раскладе много огня. Действуй активно, не бойся риска!"
    elif any(s in symbols for s in ["ᚺ", "ᛚ", "", "ᛁ"]):
        text += "💧 Водная стихия преобладает. Будь гибким, слушай чувства."
    else:
        text += "⚖️ Расклад сбалансирован. Действуй взвешенно, сохраняй спокойствие."

    # Сбрасываем сид, чтобы не ломать random в других частях бота
    random.seed()

    return text, [r1['ru'], r2['ru'], r3['ru']]